package com.revenup.labs.triggerhappy.model;

public class ActiveCampaignFilter {

	private int activeCampaignId;
	private int filterId;
	private String filter;

	public int getActiveCampaignId() {
		return activeCampaignId;
	}

	public void setActiveCampaignId(int activeCampaignId) {
		this.activeCampaignId = activeCampaignId;
	}

	public int getFilterId() {
		return filterId;
	}

	public void setFilterId(int filterId) {
		this.filterId = filterId;
	}

	public String getFilter() {
		return filter;
	}

	public void setFilter(String filter) {
		this.filter = filter;
	}

}
